//wajp to demonstrate to print default and assigned value of each datatype in 
//wajp to print the value of var(local, instance, static variable in java
public class Main{
    public static void main(String[] args) {
        int a=4;
        int var;
        var=a;
        System.out.println("Var using =: "+var);
        var+=a;
        System.out.println("Var using +=: "+var);
        var*=a;
        System.out.println("Var using *=: "+var);
}}